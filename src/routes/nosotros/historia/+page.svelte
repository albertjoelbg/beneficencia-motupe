<article class="pt-5 px-6 pb-10 m-2">
    <P class="mb-3" color="text-gray-500 dark:text-gray-400" firstupper>Las Sociedades de Beneficencia Perú, existen
        desde el siglo pasado como base histórica de la Institucionalidad de nuestro país y su vigencia aún contribuye
        en el rol social del Estado cumpliendo con los fines para los cuales se crearon.
    </P>
    <P weight="light" color="text-gray-500 dark:text-gray-400">Su origen está relacionado con el Decreto Supremo del 25
        de Abril de 1837 disponiendo que se establezcan Comisiones de Beneficencias en todas las poblaciones en que
        existen colegios o escuelas, que ya existían en Motupe.</P>
    <br>
    <P weight="light" color="text-gray-500 dark:text-gray-400">El 02 de Noviembre de 1889, durante el Gobierno del
        Presidente de Don Andrés Avelino Cáceres, se dictó una ley en la que se encarga a las Sociedades de
        Beneficencias Públicas la Administración de los bienes de cofradías, archicofradías, congregaciones de este
        género existentes
        en sus respectivas provincias, con el fin de que procedan a cumplir con las obras o potestades acrode a las
        fundaciones y el sobrante de dichas rentas se aplicarían al sostenimiento de los hospitales, casas de caridad o
        asilos.</P>
    <br>
    <P weight="light" color="text-gray-500 dark:text-gray-400">Durante el Gobierno del Presidente Remigio Morales
        Bermúdez, el 02 de Octubre de 1983, se dicta una ley que organiza a las Beneficencias Públicas de la República,
        las que tienen por único objetivo apoyar y proteger a los desvalidos. La ley las reconoce y ampara, las define
        como públicas o particulares, las primeras son fundadas, sostenidas o financiadas por el Estado o por las
        autoridades Departamentales o Municipales, las particulares son sostenidas por personas naturales.</P>
    <br>
    <P weight="light" color="text-gray-500 dark:text-gray-400">El 07 de Noviembre de 1935 Don Oscar R. Benavides,
        Presidente de la República, emite la Ley N° 8128 que reorganiza las Sociedades de Beneficencias Públicas.</P>
    <br>
    <P weight="light" color="text-gray-500 dark:text-gray-400">El <strong>02 de Septiembre de 1941</strong>, en Mérito a
        la <strong>Ley N° 9365</strong>, se crea la <strong>SOCIEDAD DE BENEFICENCIA DE MOTUPE</strong>, durante el
        Gobierno del Presidente Manuel Prado Ugarteche; surgiendo como Institución al recoger del vecindario la
        necesidad de organizarse para hacer frente a los problemas existentes en la localidad.</P>
